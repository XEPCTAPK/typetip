import * as vscode from 'vscode';

/**
 * Единый разделяемый контекст выполнения PTY-приложения.
 * Избавляет дочерние компоненты от необходимости хранить лишние ссылки на глобальные подсистемы.
 */
export interface IScreenContext {
  /** Системный низкоуровневый эмиттер для вывода ANSI-последовательностей на экран. */
  writeEmitter: vscode.EventEmitter<string>;
  /** Глобальный контекст расширения VS Code для персистентного хранения стейта/рекордов. */
  extensionContext: vscode.ExtensionContext;
  /** Актуальная ширина матрицы терминала в колонках (символах). */
  terminalWidth: number;
  /** Актуальная высота матрицы терминала в строках. */
  terminalHeight: number;
  
  // --- АДАПТИВНЫЕ ФЛАГИ ОКРУЖЕНИЯ ---
  /** Включается, если размеры окна критически малы (меньше 40x10). */
  isSafeMode: boolean;
  /** Включается, если места достаточно для полноценного UI (от 110x25). */
  isWideMode: boolean;
  /** Флаг активной светлой темы редактора VS Code. */
  isLightTheme: boolean;
}

/**
 * Универсальный интерфейс интерактивного экрана (компонента) SPTA-приложения.
 * Все экраны (HubMenu, TypeTipKeyboard, DashGame, RainGame, IntroScreen) обязаны его реализовать.
 */
export interface IScreen {
  /**
   * Вызывается роутером в момент активации экрана (монтирование компонента).
   * Здесь запускаются игровые таймеры, генерируются тексты заданий и сбрасываются счетчики.
   * @param {IScreenContext} ctx Сформированный мастер-терминалом контекст выполнения.
   */
  init(ctx: IScreenContext): void;

  /**
   * Формирует и возвращает финальный графический кадр (строку) для отрисовки.
   * Метод НЕ должен самостоятельно писать в эмиттер — он лишь генерирует ANSI-строку кадра.
   * @returns {string} Готовая ANSI-последовательность текстовой матрицы.
   */
  render(): string;

  /**
   * Центральный шлюз обработки событий ввода для текущего экрана.
   * Принимает оттранслированные роутером управляющие маркеры (UI_UP, UI_ENTER) или сырые символы.
   * @param {string} data Символ или управляющий маркер ввода.
   */
  handleInput(data: string): void;

  /**
   * Опциональное уведомление экрана об аппаратном изменении геометрии окна терминала.
   * @param {number} width Новая ширина в колонках.
   * @param {number} height Новая высота в строках.
   */
  resize?(width: number, height: number): void;

  /**
   * Опциональный деструктор экрана. Вызывается роутером при размонтировании (уходе с экрана).
   * Служит для обязательной остановки фоновых интервалов, циклов эмуляции и зачистки памяти.
   */
  dispose?(): void;
}